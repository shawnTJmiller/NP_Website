package com.techelevator.model;

public class TopSurvey {

	private String parkCodeSurvey;
	private String parkNameSurvey;
	private int parkSurveyCount;
	
	
	public String getParkCodeSurvey() {
		return parkCodeSurvey;
	}
	public void setParkCodeSurvey(String parkCodeSurvey) {
		this.parkCodeSurvey = parkCodeSurvey;
	}
	public String getParkNameSurvey() {
		return parkNameSurvey;
	}
	public void setParkNameSurvey(String parkNameSurvey) {
		this.parkNameSurvey = parkNameSurvey;
	}
	public int getParkSurveyCount() {
		return parkSurveyCount;
	}
	public void setParkSurveyCount(int parkSurveyCount) {
		this.parkSurveyCount = parkSurveyCount;
	}
	
}